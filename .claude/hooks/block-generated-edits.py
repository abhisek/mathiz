#!/usr/bin/env python3
"""PreToolUse hook: block hand-edits to generated ent code.

Everything under ent/ except ent/schema/ and ent/generate.go is generated by
`make generate`. Hand-edits there are silently overwritten by the next
codegen run — the fix belongs in ent/schema/.

Exit 2 blocks the tool call; stderr is fed back to the agent.
"""
import json
import os
import sys

try:
    payload = json.load(sys.stdin)
except json.JSONDecodeError:
    sys.exit(0)

path = (payload.get("tool_input") or {}).get("file_path", "")
if not path:
    sys.exit(0)

# Normalize to a repo-relative path.
rel = os.path.relpath(path, os.getcwd()) if os.path.isabs(path) else path
rel = rel.replace(os.sep, "/")

if not rel.startswith("ent/"):
    sys.exit(0)
if rel.startswith("ent/schema/") or rel == "ent/generate.go":
    sys.exit(0)

sys.stderr.write(
    f"{rel} is GENERATED code (ent codegen). Do not hand-edit it — "
    "change the schema in ent/schema/ instead, then run `make generate`.\n"
)
sys.exit(2)
